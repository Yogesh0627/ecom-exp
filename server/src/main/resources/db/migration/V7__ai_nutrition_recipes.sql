-- =============================================================================
-- V7 — AI, Nutrition & Recipes
-- Backs the five hero features (PRD §5):
--   1. Smart Fridge AI      -> fridge_scans, fridge_scan_items
--   2. Smart Cart Nutrition -> nutrition_facts (V2) + cart_nutrition_summary view
--   3. Recommendations      -> recommendation_rules
--   4. Weekly Meal Planner  -> meal_plans, meal_plan_entries
--   5. AI Pantry            -> pantry_items
